sub roundMatrix { # matrix precision
    my ( $m, $p ) = @_;
    my ( $r, $c ) = $m->dimensions;
    my $res = Value::Matrix->I($r);
    for (my $i = 0; $i < $r; $i++) { 
       for (my $j = 0; $j < $c; $j++) {
	   my $e = $m->element($i+1,$j+1);
	   assign( $res, [$i,$j], Round( $e, $p ) ); } }
    return $res;
}

sub easyReciprocalQ {
    my $n = shift;
    my $c = 0;
    until ( $n % 2 ) {
        $n = $n / 2;
        $c += 1; }
    until ( $n % 5 ) {
        $n = $n / 5;
        $c -= 1; }
    return $n == 1 && $c < 5 && $c > -5;
}

sub randEasyReciprocal{
    my $max = shift;
    my $r = 3;
    while ( ! &easyReciprocalQ( $r ) ) {
        $r = random( 2, $max, 1); }
    return $r;
}

