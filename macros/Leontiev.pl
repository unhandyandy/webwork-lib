sub randLeontiev {
    my $n = shift;
    if ( $n==3 ) {
        return Matrix( $Leontiev::Leontiev3x3ArrayList[random(0,999,1)] );
    } else {
        return randLeontievGen($n);
    }
}

sub checkTech{
    my ( $A, $summer, $n ) = @_;
    my $idmat = Value::Matrix->I( $n );
    my $checkmat = 10*($idmat - $A);
    my $check = abs( $checkmat->det->value );
    my $zerocheckmat = $summer * $A;
    my $zerocheckC = min( $zerocheckmat->row(1)->value );
    $zerocheckmat = $A * $summer;
    my $zerocheckR = min( $zerocheckmat->transpose()->row(1)->value );
    #die "zerocheck: $zerocheckR";
    return easyReciprocalQ($check) && $zerocheckR != 0 && $zerocheckC != 0;
}



