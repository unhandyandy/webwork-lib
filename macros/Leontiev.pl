sub randLeontievGen {
    my $n = shift;
    my $diag = rand_diag_matrix($n);
    my $diag_inv = Value::Matrix->I($n);
    for (my $i=0; $i<$n; $i++){
	my $e = $diag->element($i+1,$i+1);
	assign( $diag_inv, [$i,$i], 1/$e ); }
    my @ones = map { Real(1.0) } (1..$n);
    my $summer = Vector( @ones );
    my $res;
    do  {
        $res = random_matrix( $n ); 
        my $sums = Vector( $summer * $res );
        my @reducer = map { 1/$_ } $sums->value;
        my $reduceMat = makeDiagMat( @reducer );
        $res = $res * $reduceMat;
        $res = ($diag * $res) * ($diag_inv);
        $res = roundMatrix( $res, 1 );
    } until ( checkTech( $res, $summer, $n ) );
    return $res;
}

